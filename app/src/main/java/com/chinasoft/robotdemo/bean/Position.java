package com.chinasoft.robotdemo.bean;

public class Position {

    /**
     * X坐标
     */
    private double x;

    /**
     * Y坐标
     */
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Position{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
